#include <iostream>
using namespace std;

int main(){
    /*while반복문으로 특정 수 만큼 반복하기1
     *  : while반복문을 사용해서 메시지를 10회 출력하기*/

    // 카운터 선언하고 초기화
    int counter =0;

    //while반복문
    while(counter<10){
        cout <<"Hello world!"<<endl;
        counter++;
    }

    return 0;
}
